package com.example.calculator;

public class Calculator {

    public void toAdd(double num1, double num2, double result) {
        result = num1 + num2;
    }
    public void toSubtract(double num1, double num2, double result) {
        result = num1 - num2;
    }
    public void toDivide(double num1, double num2, double result) {
        result = num1 / num2;
    }
    public void toMultiply(double num1, double num2, double result) {
        result = num1 * num2;
    }


}
